public abstract class HeroTrap implements Bundlable {
	//나중에 쓸거임
	
	public int image;
	public int pos;


	public void trigger(){

		Char ch = Actor.findChar(pos);
		if (Dungeon.level.heroFOV[pos]) {
			Sample.INSTANCE.play(Assets.Sounds.TRAP);
		}

		if (ch instanceof Hero){
			((Hero) ch).interrupt();
		}

		wither();
		activate(ch );
	}

	public abstract void activate( Char ch );

	public void wither() {
	//	Dungeon.level.uptrap( pos );

		if (Dungeon.level.heroFOV[pos]) {
			CellEmitter.get( pos ).burst( BloodParticle.FACTORY, 6 );
		}

	}
	
	private static final String POS	= "pos";

	@Override
	public void restoreFromBundle( Bundle bundle ) {
		pos = bundle.getInt( POS );
	}

	@Override
	public void storeInBundle( Bundle bundle ) {
		bundle.put( POS, pos );
	}

	public String name(){
		return Messages.get(this, "name");
	}

	public String desc() {
		String desc = Messages.get(this, "desc");
		if (Dungeon.hero.subClass == HeroSubClass.SOULCHASER){
			desc += "\n\n" + Messages.get(this, "warden_desc");
		}
		return desc;
	}
}
