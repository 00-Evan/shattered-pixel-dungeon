package com.shatteredpixel.shatteredpixeldungeon.items.food;


import com.shatteredpixel.shatteredpixeldungeon.actors.buffs.Buff;
import com.shatteredpixel.shatteredpixeldungeon.actors.buffs.Hunger;
import com.shatteredpixel.shatteredpixeldungeon.actors.buffs.SuperBulk;
import com.shatteredpixel.shatteredpixeldungeon.actors.hero.Hero;
import com.shatteredpixel.shatteredpixeldungeon.messages.Messages;
import com.shatteredpixel.shatteredpixeldungeon.sprites.ItemSpriteSheet;
import com.shatteredpixel.shatteredpixeldungeon.utils.GLog;


public class SuperMushroom extends Food {

	{
		image = ItemSpriteSheet.MUSHROOM;
		energy = Hunger.HUNGRY/2f;
	}
	
	@Override
	protected void satisfy(Hero hero) {
		super.satisfy(hero);
		Buff.affect(hero, SuperBulk.class).reset();
		SuperBulk superBulk = Buff.affect(hero, SuperBulk.class);
		superBulk.reset();

		hero.updateHT( true );

		GLog.i( Messages.get(SuperMushroom.class, "bulk") );
	}
	
	public int value() {
		return 10 * quantity;
	}

}
