public class LapidaristBuff extends Buff {
	//수집가의 실시간 링 버프 체크
	{
		type = buffType.NEUTRAL;
		revivePersists = true;
	}

	public void RingCheck(){
		Ring ring = null;
		for (Ring r : hero.belongings.getAllItems(Ring.class)) {

			if (ring == null || (r.isIdentified() && !(r.cursed))) {
				ring = r;
			}
			if (ring != null && (r.isIdentified() && !(r.cursed))) {
				r.activate(hero);
				Dungeon.hero.updateHT( false );
			}
		}
	}

	@Override
	public boolean act(){
		//수집가는 링을 얻을때 효과 적용

		if (Dungeon.hero.subClass == HeroSubClass.LAPIDARIST) {
			RingCheck();
			spend(TICK);
			return true;
		}

		return true;
	}

}
